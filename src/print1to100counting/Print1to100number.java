package print1to100counting;

public class Print1to100number {

    public static void main(String[] args) {
        int one = 'A' / 'A';   //1
        String s1 = "..........";

        for (int i = one; i <= (s1.length() * s1.length()); i++) {
            System.out.println(i);

        }

    }
}